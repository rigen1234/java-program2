public class Man{
	private String name;
	private String id;
	private String dept;
	public void setInfo(String name,String id,String dept)
	{
		this.name=name;
		this.id=id;
		this.dept=dept;
	}
	public String getname()
	{
		return name;
	}
	public String getid()
	{
		return id;
	}
	public String getdept()
	{
		return dept;
	}
}